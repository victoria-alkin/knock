-- Enable realtime for messages so the unread dot updates live. RLS still
-- applies: a client only receives inserts for conversations it participates in.

do $$
begin
  if not exists (
    select 1 from pg_publication_tables
    where pubname = 'supabase_realtime'
      and schemaname = 'public'
      and tablename = 'messages'
  ) then
    alter publication supabase_realtime add table public.messages;
  end if;
end $$;
